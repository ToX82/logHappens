                    or the <a href="<?= buildUrl("display/troubleshooting") ?>">Troubleshooting</a>
                    page. And if you find some technical issues, please open an issue on
                    <a href="https://github.com/ToX82/logHappens/issues">GitHub</a>.
                </p>

